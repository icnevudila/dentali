/**
 * Builds src/lib/i18n/tr-ui-strings.ts from messagesTr + TR_EXACT + overrides.
 * Run: npx tsx scripts/build-tr-ui-strings.mjs
 */
import fs from "node:fs"
import path from "node:path"
import { codeMessagesEn } from "../src/lib/i18n/code-messages.ts"
import { messagesEnPh, messagesTr } from "../src/lib/i18n/messages.ts"
import { codeMessagesTrOverrides } from "../src/lib/i18n/code-messages-tr-overrides.ts"
import { TR_EXACT_EXPORT } from "../src/lib/i18n/fallback-translations.ts"

function flatten(tree, prefix = "") {
  const out = {}
  for (const [key, value] of Object.entries(tree)) {
    const p = prefix ? `${prefix}.${key}` : key
    if (typeof value === "string") out[p] = value
    else Object.assign(out, flatten(value, p))
  }
  return out
}

function isHybridTr(text) {
  if (!/[ğüşıöçĞÜŞİÖÇ]/.test(text)) return false
  return /\b(the|and|for|with|from|who|which|this|that|before|after|without|showing|review|use|read|open|select|need|your)\b/i.test(
    text
  )
}

function isProperNoun(en) {
  return /^(PhilHealth|HMO|SMS|OK|Blog|SKU|Kiosk|Portal|Global|PDA|Queue|Chart|SEO|CSV|PDF|PHP|₱|dry-run|Self-pay)$/i.test(
    en.trim()
  )
}

const enMsg = flatten(messagesEnPh)
const trMsg = flatten(messagesTr)
const enCode = flatten(codeMessagesEn)
const overrides = flatten(codeMessagesTrOverrides)

const trByEnglish = { ...TR_EXACT_EXPORT }

for (const [key, en] of Object.entries(enMsg)) {
  const tr = trMsg[key]
  if (tr && tr !== en && !isHybridTr(tr)) trByEnglish[en] = tr
}

for (const [key, tr] of Object.entries(overrides)) {
  const en = enCode[key]
  if (en && tr) trByEnglish[en] = tr
}

// Keyed manual additions (namespace.key → tr) for code-only strings
const BY_KEY = {
  ...JSON.parse(fs.readFileSync(path.join(process.cwd(), "scripts/tr-by-key.json"), "utf8")),
  ...(fs.existsSync(path.join(process.cwd(), "scripts/phrase-tr-by-key.json"))
    ? JSON.parse(fs.readFileSync(path.join(process.cwd(), "scripts/phrase-tr-by-key.json"), "utf8"))
    : {}),
}

for (const [key, tr] of Object.entries(BY_KEY)) {
  const en = enCode[key]
  if (en && tr) trByEnglish[en] = tr
}

let covered = 0
let missing = 0
for (const en of new Set(Object.values(enCode))) {
  if (trByEnglish[en] || isProperNoun(en)) covered++
  else missing++
}

const sorted = Object.fromEntries(
  Object.entries(trByEnglish).sort(([a], [b]) => a.localeCompare(b))
)

const out = `// AUTO-GENERATED by scripts/build-tr-ui-strings.mjs — do not edit by hand.
// Run: npm run i18n:build-tr

export const TR_UI_STRINGS: Record<string, string> = ${JSON.stringify(sorted, null, 2)}

export function translateUiString(english: string): string | undefined {
  return TR_UI_STRINGS[english]
}
`

fs.writeFileSync(path.join(process.cwd(), "src/lib/i18n/tr-ui-strings.ts"), out, "utf8")
console.log(`TR_UI_STRINGS: ${Object.keys(sorted).length} entries`)
console.log(`Code EN strings covered: ${covered}, still missing TR: ${missing}`)
