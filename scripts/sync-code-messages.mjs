/**
 * Scans src/ for t("key", "fallback") calls missing from messages.ts,
 * then writes src/lib/i18n/code-messages.ts with EN + auto TR translations.
 *
 * Run: npm run i18n:sync
 */
import fs from "node:fs"
import path from "node:path"
import { messagesEnPh, messagesTr } from "../src/lib/i18n/messages.ts"
import { codeMessagesTrOverrides } from "../src/lib/i18n/code-messages-tr-overrides.ts"
import { translateUiString } from "../src/lib/i18n/tr-ui-strings.ts"

const phraseTrByKeyPath = path.join(process.cwd(), "scripts/phrase-tr-by-key.json")
const phraseTrByKey = fs.existsSync(phraseTrByKeyPath)
  ? JSON.parse(fs.readFileSync(phraseTrByKeyPath, "utf8"))
  : {}

function isHybridTr(text) {
  if (!/[ğüşıöçĞÜŞİÖÇ]/.test(text)) return false
  return /\b(the|and|for|with|from|who|which|this|that|before|after|without|showing|review|use|read|open|select|need|your)\b/i.test(
    text
  )
}

function flatten(tree, prefix = "") {
  const out = {}
  for (const [key, value] of Object.entries(tree)) {
    const p = prefix ? `${prefix}.${key}` : key
    if (typeof value === "string") out[p] = value
    else Object.assign(out, flatten(value, p))
  }
  return out
}

function setNested(tree, dottedKey, value) {
  const parts = dottedKey.split(".")
  let cur = tree
  for (let i = 0; i < parts.length - 1; i++) {
    const part = parts[i]
    if (typeof cur[part] !== "object" || cur[part] === null) cur[part] = {}
    cur = cur[part]
  }
  cur[parts[parts.length - 1]] = value
}

function sortTree(tree) {
  const out = {}
  for (const key of Object.keys(tree).sort()) {
    const val = tree[key]
    out[key] = typeof val === "object" && val !== null ? sortTree(val) : val
  }
  return out
}

function treeToTs(obj, indent = 2) {
  const pad = " ".repeat(indent)
  const lines = ["{"]
  for (const [key, val] of Object.entries(obj)) {
    const safeKey = /^[a-zA-Z_][a-zA-Z0-9_]*$/.test(key) ? key : JSON.stringify(key)
    if (typeof val === "string") {
      lines.push(`${pad}${safeKey}: ${JSON.stringify(val)},`)
    } else {
      lines.push(`${pad}${safeKey}: ${treeToTs(val, indent + 2)},`)
    }
  }
  lines.push(`${" ".repeat(indent - 2)}}`)
  return lines.join("\n")
}

const enCatalog = flatten(messagesEnPh)
const trCatalog = flatten(messagesTr)
const overridesFlat = flatten(codeMessagesTrOverrides)

const srcRoot = path.join(process.cwd(), "src")
const tCallRe = /\bt\(\s*["'`]([^"'`]+)["'`]\s*,\s*["'`]((?:\\.|[^"'`])*)["'`]/gs

function walk(dir, files = []) {
  for (const name of fs.readdirSync(dir)) {
    const full = path.join(dir, name)
    if (fs.statSync(full).isDirectory()) {
      if (name !== "node_modules" && name !== "lib" || full.includes("lib")) {
        if (name === "node_modules") continue
        walk(full, files)
      }
    } else if (/\.(tsx?|jsx?)$/.test(name)) {
      if (!full.includes("code-messages.ts")) files.push(full)
    }
  }
  return files
}

// Fix walk - simpler
function walkSrc(dir, files = []) {
  for (const name of fs.readdirSync(dir)) {
    const full = path.join(dir, name)
    const stat = fs.statSync(full)
    if (stat.isDirectory()) {
      if (name !== "node_modules") walkSrc(full, files)
    } else if (/\.(tsx?|jsx?)$/.test(name) && !full.endsWith("code-messages.ts")) {
      files.push(full)
    }
  }
  return files
}

const KEY_PATTERN = /^[a-z][a-z0-9]*(?:\.[a-zA-Z][a-zA-Z0-9]*)+$/

const used = new Map()
for (const file of walkSrc(srcRoot)) {
  const text = fs.readFileSync(file, "utf8")
  let m
  while ((m = tCallRe.exec(text))) {
    const key = m[1]
    if (!KEY_PATTERN.test(key)) continue
    const fallback = m[2].replace(/\\'/g, "'").replace(/\\"/g, '"').replace(/\\n/g, "\n")
    if (!used.has(key) || used.get(key).length < fallback.length) {
      used.set(key, fallback)
    }
  }
}

const codeEn = {}
const codeTr = {}
let added = 0

for (const [key, fallback] of used) {
  if (key in enCatalog) continue
  setNested(codeEn, key, fallback)
  const trExisting = trCatalog[key]
  const trFromPhrase = phraseTrByKey[key] ?? overridesFlat[key]
  const trValue =
    trFromPhrase ??
    (trExisting && trExisting !== fallback && !isHybridTr(trExisting)
      ? trExisting
      : translateUiString(fallback) ?? fallback)
  setNested(codeTr, key, trValue)
  added++
}

const sortedEn = sortTree(codeEn)
const sortedTr = sortTree(codeTr)

const outPath = path.join(process.cwd(), "src/lib/i18n/code-messages.ts")
const header = `// AUTO-GENERATED by scripts/sync-code-messages.mjs — do not edit by hand.
// Run: npm run i18n:sync
import type { MessageTree } from "./messages"

`

const body = `${header}export const codeMessagesEn: MessageTree = ${treeToTs(sortedEn)}

export const codeMessagesTr: MessageTree = ${treeToTs(sortedTr)}
`

fs.writeFileSync(outPath, body, "utf8")
console.log(`Wrote ${added} code-message keys to ${outPath}`)
